    private void btn_tambah1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambah1ActionPerformed
        // TODO add your handling code here:
        String inv = "";
        Date d = new Date();
        DateFormat dateformat = new SimpleDateFormat("dd-MM-YYYY-HH-mm-ss");
        DateFormat dateformat2 = new SimpleDateFormat("YYYY-MM-dd HH:mm:ss");
        inv = dateformat.format(d);
        double diskon = Double.parseDouble(combo_diskon1.getSelectedItem().toString()) /100;
        String[] split = combo_paket1.getSelectedItem().toString().split(":");
        int total = Integer.parseInt(text_qty1.getText()) * Integer.parseInt(split[2]);
        int pajak = hitungPajak(total);
        try {
            this.stat = k.getCon().prepareStatement("insert into tb_transaksi "
            + "values (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
            stat.setInt(1, 0);
            stat.setString(2, text_idoutlet.getText());
            stat.setString(3, inv);
            stat.setString(4, combo_member1.getSelectedItem().toString());
            stat.setString(5, dateformat2.format(tgl_transaksi.getDate()));
            stat.setString(6, dateformat2.format(tgl_bataswaktu1.getDate()));
            stat.setString(7, null);
            stat.setString(8, text_biaya1.getText());
            stat.setDouble(9, diskon);
            stat.setInt(10, pajak);
            stat.setString(11, "proses");
            stat.setString(12, "belum dibayar");
            stat.setString(13, text_iduser.getText());
            stat.setString(14, split[0]);
            stat.setString(15, text_qty1.getText());
            stat.setString(16, text_keterangan1.getText());
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_tambah1ActionPerformed

