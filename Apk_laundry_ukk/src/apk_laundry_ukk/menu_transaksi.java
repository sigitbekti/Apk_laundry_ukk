     public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Transaksi");
        model.addColumn("Kode Invoice");
        model.addColumn("ID Member");
        model.addColumn("Tgl Transaksi");
        model.addColumn("Batas Waktu");
        model.addColumn("Tanggal Bayar");
        model.addColumn("Biaya Tambahan");
        model.addColumn("Diskon");
        model.addColumn("Status");
        model.addColumn("Dibayar");
        model.addColumn("ID Paket");
        model.addColumn("QTY");
        tabel_transaksi.setModel(model);
        try{
            this.stat = k.getCon().prepareStatement("select * from tb_transaksi");
            this.rs = stat.executeQuery();
            while (rs.next()) {
                Object[] data = {
                rs.getString("id_transaksi"),
                rs.getString("kode_invoice"),
                rs.getString("id_member"),
                rs.getString("tgl"),
                rs.getString("batas_waktu"),
                rs.getString("tgl_bayar"),
                rs.getString("biaya_tambahan"),
                rs.getString("diskon"),
                rs.getString("status"),
                rs.getString("dibayar"),
                rs.getString("id_paket"),
                rs.getString("qty")
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_keterangan1.setText("");
        text_biaya1.setText("");
        text_qty1.setText("");
        text_invoice.setText("");
    }
     
     public void refreshComboMember(){
         try {
             this.stat = k.getCon().prepareStatement("select * from tb_member");
             this.rs = stat.executeQuery();
             while (rs.next()) {                 
                 combo_member1.addItem(rs.getString("id_member"));
             }
         } catch (Exception e) {
             JOptionPane.showMessageDialog(null, e.getMessage());
         }
     }
     
     public void refreshComboPaket(){
         try {
             this.stat = k.getCon().prepareStatement("select * from tb_paket");
             this.rs = stat.executeQuery();
             while (rs.next()) {                 
                 combo_paket1.addItem(rs.getString("id_paket")+":"
                 +rs.getString("nama_paket")+":"
                 +rs.getString("harga"));
             }
         } catch (Exception e) {
             JOptionPane.showMessageDialog(null, e.getMessage());
         }
     }
     
     public int hitungPajak (int harga){
         int pajak = 0;
         pajak = (int) (harga * 0.1);
         return pajak;
     }

